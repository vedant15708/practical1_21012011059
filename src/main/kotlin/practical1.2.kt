fun main(){
    var num1:Int=12
    var num2:Double=num1.toDouble()
    println("$num2: Integer to double conversion")
    var s1:String="112"
    var num3:Double=s1.toDouble()
    println("$num3: String to double conversion")
    var s2:String="10"
    var num4:Int=s2.toInt()
    println("$num4: String to integer conversion")
}