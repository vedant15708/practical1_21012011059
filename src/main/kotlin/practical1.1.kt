    var a:Int=5
    var b:Double=22.3
    var c:Float=1.5F
    var d:Char='v'
    var e:Boolean=true
    var f:Long=256432467
    var g:Short=-2
    var h:Byte=123
    var i:String="vsp"

    println("$a:integer variable")
